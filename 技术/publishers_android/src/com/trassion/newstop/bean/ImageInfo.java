package com.trassion.newstop.bean;

public class ImageInfo extends JavaBean{

	/**
	 * 
	 */
	private static final long serialVersionUID = -4543466678670436845L;
	
	private String thumbnail_img;
	private String medium_img;
	private String post_thumbail_img;
	private String full_img;
	
	
	public String getThumbnail_img() {
		return thumbnail_img;
	}
	public void setThumbnail_img(String thumbnail_img) {
		this.thumbnail_img = thumbnail_img;
	}
	public String getMedium_img() {
		return medium_img;
	}
	public void setMedium_img(String medium_img) {
		this.medium_img = medium_img;
	}
	public String getPost_thumbail_img() {
		return post_thumbail_img;
	}
	public void setPost_thumbail_img(String post_thumbail_img) {
		this.post_thumbail_img = post_thumbail_img;
	}
	public String getFull_img() {
		return full_img;
	}
	public void setFull_img(String full_img) {
		this.full_img = full_img;
	}
	
	
	
	
	

}
