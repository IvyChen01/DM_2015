<script>
    <?php echo "pageIndex=".$_nav_index.";"; ?>
    $(function(){
        pageIndex=(pageIndex||0)?pageIndex-1:0;
        var curpage=$(".rt-nav").eq(pageIndex);
        if(!!pageIndex) $(".nav-bom").addClass("over");
        $(".nav-bom").width(curpage.width()).css("left",curpage.get(0).offsetLeft+parseInt(curpage.css("padding-left"))+parseInt(curpage.css("margin-left")))
        $(".rt-nav a").mouseover(function(){
            $(".subnav-panel").stop(true,true).animate({height:159});
            $(".list").hide().eq($(".rt-nav").index($(this).closest(".rt-nav"))).show();
            if(!$(".nav-bom").hasClass("over")) $(".nav-bom").addClass("over")
            $(".subnav-panel").height(159);
            var marLeft = isNaN(parseInt($(this).closest(".rt-nav").css("margin-left"))) ? 0 : parseInt($(this).closest(".rt-nav").css("margin-left"));
            var padLeft = isNaN(parseInt($(this).closest(".rt-nav").css("padding-left"))) ? 0 : parseInt($(this).closest(".rt-nav").css("padding-left"));
            var offLeft = isNaN(parseInt($(this).closest(".rt-nav").get(0).offsetLeft)) ? 0 : $(this).closest(".rt-nav").get(0).offsetLeft;
            $(".nav-bom").width($(this).closest(".rt-nav").width()).css("left",marLeft+padLeft+offLeft)
        })
        $("#header").mouseleave(function(){
            if($(".subnav-panel").height()!=0){
                $(".subnav-panel").stop(true,true).animate({height:0},function(){ $(".list").hide();});
            }
            if(!!pageIndex) $(".nav-bom").addClass("over");
            $(".nav-bom").width(curpage.width()).css("left",curpage.get(0).offsetLeft+parseInt(curpage.css("padding-left"))+parseInt(curpage.css("margin-left")))
        })
        $(".sub-nav li").mouseover(function(){
            $(".sub-nav li").stop().children(".copy").width(0)
            $(this).children(".copy").animate({"width":$(this).width()},50,"linear")
        }).mouseleave(function(){
            $(this).children(".copy").stop().width(0);
        })
    })
</script>