<script>
    window.onload = function () {
        (function () {

            var ft_f=$$(".rt-nav"),
                ft_f_len=ft_f.length;
            for(i=0;i<ft_f_len;i++){
                ft_f[i].index=i;
                ft_f[i].onmouseover=function(){
                    var bot=$$(".nav-bom")[0],
                        bot_left=parseInt(this.offsetLeft)+parseInt(getStyle(this,"paddingLeft"))+parseInt(getStyle(this,"marginLeft")),
                        bot_width=getStyle(this,"width");
                    with(bot.style){
                        left=bot_left+"px";
                        width=bot_width;
                    }
                    //show
                    $$(".list")[0].style.height=0;
                    var list=$$(".list"),
                        list_len=list.length;
                    for(j=0;j<list_len;j++){
                        if(j!=this.index&&list[j]){
                            with(list[j].style){
                                zIndex=0;
                                height="0";
                            }
                        }
                    }
                    if(list[this.index]){
                        with(list[this.index].style){
                            zIndex=1;
                            height="159px";
                        }
                        var index=this.index;
                        list[index].onmouseleave=function(){
                            with(list[index].style){
                                height=0;
                                zIndex=0;
                            }
                        }
                    }
                }
            }
        })();



    }
    function getStyle(el,attr){return el.currentStyle?el.currentStyle[attr]:window.getComputedStyle(el,null)[attr];}
    function $$(selector){return document.querySelectorAll(selector);}
</script>