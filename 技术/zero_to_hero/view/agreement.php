function onClickStart(e)
{
	if (!isStart)
	{
		if ("" == $("#nickTxt").val())
		{
			alert("Please enter nickname.");
			$("#nickTxt").focus();
			return;
		}
		if ("" == $("#emailTxt").val())
		{
			alert("Please enter email.");
			$("#emailTxt").focus();
			return;
		}
		
		if (!checkEmail($("#emailTxt").val()))
		{
			alert("Please enter the correct email!");
			$("#emailTxt").focus();
			return;
		}
		
		if ($('#acceptChk:checked').val())
		{
			isStart = true;
			login();
		}
		else
		{
			alert("Please read and accept the USER AGREEMENT first.");
		}
	}
}

function onTestStart(e)
{
	if (!isStart)
	{
		if ("" == $("#nickTxt").val())
		{
			alert("Please input nickname.");
			$("#nickTxt").focus();
			return;
		}
		if ("" == $("#emailTxt").val())
		{
			alert("Please input email.");
			$("#emailTxt").focus();
			return;
		}
		if ($('#acceptChk:checked').val())
		{
			isStart = true;
			$.post("?m=fbzero&a=agreement", {nick: $("#nickTxt").val(), email: $("#emailTxt").val()}, onChange);
		}
		else
		{
			alert("Please read and accept the USER AGREEMENT first.");
		}
	}
}

function onChange(value)
{
	var res = null;
	
	if (value.substr(0, 1) != "{")
	{
		isStart = false;
		alert("Unknown Error!");
		return;
	}
	
	res = $.parseJSON(value);
	switch (res.code)
	{
		case 0:
			document.location.href = "./";
			break;
		default:
			isStart = false;
			alert(res.info);
	}
}

